def get_all_exercises():
    """Получить все упражнения"""
    return EXERCISES

def get_exercise_by_category(category: str):
    """Получить упражнения по категории"""
    return [ex for ex in EXERCISES if ex['category'] == category]

def get_random_exercise():
    """Получить случайное упражнение"""
    import random
    return random.choice(EXERCISES)

def get_exercise_categories():
    """Получить все категории упражнений"""
    categories = set()
    for ex in EXERCISES:
        categories.add(ex['category'])
    return sorted(list(categories))
